'''
1. По клиенту получены зашумленные данные (объект s1 типа Series) по его транзакциям:
s1 = pd.Series(data=['7', 4, 8.3, 'hi!', 15, -52, 12.42, 's123', 120.1, 58], index=range(6, 26, 2))
Выберите из s1 элементы с индексами 8 и 14, сложите их, из полученного результата
вычтите количество целочисленных элементов (имеющих тип int), ответ сохраните в
переменную s2.
'''

import pandas as pd

s1 = pd.Series(data=['7', 4, 8.3, 'hi!', 15, -52, 12.42, 's123', 120.1, 58], index=range(6, 26, 2))

'''
6       '7'         индекс 6 → строка '7'
8       4           индекс 8 → целое 4
10      8.3         индекс 10 → float 8.3
12      'hi!'       индекс 12 → строка 'hi!'
14      15          индекс 14 → целое 15
16      -52         индекс 16 → целое -52
18      12.42       индекс 18 → float 12.42
20      's123'      индекс 20 → строка 's123'
22      120.1       индекс 22 → float 120.1
24      58          индекс 24 → целое 58
'''

print('Исходный Series s1:')
print(s1, '\n')

elem8 = s1[8]  # 4
elem14 = s1[14]  # 15

print(f'Элемент с индексом 8: {elem8}')
print(f'Элемент с индексом 14: {elem14} \n')

# Сложение
sum_elements = elem8 + elem14
print(f'Сумма элементов: {elem8} + {elem14} = {sum_elements} \n')

# Считаем количество целочисленных элементов
int_count = 0
for i in s1:
    if isinstance(i, int):
        int_count += 1
print(f'Количество целочисленных элементов в s1: {int_count} \n')

# Вычитаем
s2 = sum_elements - int_count
print(f'Итоговый результат: {sum_elements} - {int_count} = {s2}')
